package com.zy.loop;

public class ForAndWhile {

	public static void main(String[] args) {
		forTest();
		whileTest();
	}

	public static void forTest() {
		for (;;) {
			
		}
	}
	
	public static void whileTest() {
		while (true) {
			
		}
	}
	
	public void switchTest() {
		switch (0) {
		case 0:
			
			break;

		default:
			break;
		}
	}
}
